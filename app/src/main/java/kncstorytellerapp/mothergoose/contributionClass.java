public class contributionClass {

    String storyID;
    String contributorID;
    String getContributorName;
    String context;
    String status;

    public String getStoryID() {
        return storyID;
    }

    public void setStoryID(String storyID) {
        this.storyID = storyID;
    }

    public String getContributorID() {
        return contributorID;
    }

    public void setContributorID(String contributorID) {
        this.contributorID = contributorID;
    }

    public String getGetContributorName() {
        return getContributorName;
    }

    public void setGetContributorName(String getContributorName) {
        this.getContributorName = getContributorName;
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }



    public contributionClass(){}

}
